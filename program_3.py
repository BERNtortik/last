a1 = float(input("введите первый член прогрессии a1: ")) 
d = float(input("введите шаг d: ")) 
n = int(input("введите количество членов n: ")) 
progression = [] 
for n in range(n): 
    gin = a1 + n * d 
progression.append(gin)  
print("первые", n, "членов арифметической прогрессии:") 
for ginil in progression: 
    print(ginil, end=" ")
